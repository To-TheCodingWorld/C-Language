#include<stdio.h>
int main()
{
    int first , second;
     printf(" Enter the first number : ");
     scanf("%d",&first);

     printf("Enter the second number : ");
     scanf("%d",&second);

     printf("The Addition is : %d\n",first+second);
     printf("The Substraction  is : %d\n",first-second);
     printf("The Multification  is : %d\n",first*second);
     printf("The Division  is : %d\n",first/second);
     printf("The Remainder  is : %d\n",first%second);
     return 0;   

}